package basicjava;
import java.util.Scanner;
public class Substraction {
public static void main(String[] args) {
	Scanner sc=new Scanner(System.in);
	System.out.println("enter substration of two numbers");
	int C=sc.nextInt();
	int D=sc.nextInt();
	int S=C-D;
	System.out.println("substration:"+S);
}
}
